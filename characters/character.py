class Character:
    def __init__(self, name="unknown",
                 health=1,
                 constitution=1,
                 dexterity=1,
                 intelligence=1,
                 wisdom=1):
        self.name = name
        self.health = health
        self.constitution = constitution
        self.dexterity = dexterity
        self.intelligence = intelligence
        self.wisdom = wisdom